const getDateFromNowAndAddDaysAndSetHoursAndMinutes = (
  days: number,
  hours: number,
  minutes: number,
) => {
  const date = new Date()
  date.setDate(date.getDate() + days)
  date.setHours(hours)
  date.setMinutes(minutes)
  return date
}

